answer = ["イギリス","イギリス","スペイン","ドイツ","フランス","イギリス",
          "スペイン","ドイツ","イタリア","スペイン","ドイツ","イタリア",
          "スペイン","ドイツ","イタリア","ドイツ","フランス","イギリス",
          "スペイン","ドイツ","イギリス","スペイン","ドイツ","イギリス",
          "スペイン","ドイツ","イギリス","スペイン","ドイツ","イタリア",
          "スペイン","ドイツ","イタリア","イタリア","スペイン","ドイツ",
          "イタリア","イタリア","スペイン","ドイツ","イタリア","ドイツ",
          "イタリア","イタリア","スペイン","ドイツ"]

#空の辞書を用意
results = {}

#辞書resultsに国と得票数を格納する
for country in answer:
    if country in results:
        results[country] += 1
    else:
        results[country] = 1

#結果を表示する
for country,num in results.items():
    print("{}: {}".format(country, num))
